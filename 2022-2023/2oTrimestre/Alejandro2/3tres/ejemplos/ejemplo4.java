package ejemplos;

public class ejemplo4 {

	public static void main(String[] args) {
		

		int numero =1;
		while(numero<=10){ //bucle que cuenta hasta 10
		System.out.println(numero);
		numero++;
		}
		
		numero=1;
		do {
			System.out.println(numero);
			
			numero++;
		}while(numero<=10);
		
		
		for(int i=1;i<=10;i++){
			
			System.out.println(i);
		}
	
		
		
		
for(int i=10;i>=1;i--){
			
			System.out.println(i);
		}
		System.out.println("fin de codigo");
	}

}
