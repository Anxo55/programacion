package resueltos;

public class minumero {
    
    
	
	double num;
	public  double doble() {
		double resultado=num*2;
		return resultado;

	}

	public  double triple() {
	double resultado=num*3;
	return resultado;
	}

	public  double cuadruple() {
        double resultado=num*4;
        return resultado;
        }

		minumero(double c){
			this.num=c;
		}
	
		public  double multiplicacion(double a) {
            double resultado=num*a;
            return resultado;
            }
	
}
